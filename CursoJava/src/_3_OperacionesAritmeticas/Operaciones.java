package _3_OperacionesAritmeticas;

public class Operaciones {

    public static void main(String[] args) {
        
        int num1, num2;
        
        num1 = 9;
        num2 = 3;
        
        int resultado = num1 + num2;
        
        System.out.println(resultado);
        System.out.println(num1 + num2);
        System.out.println(num1 - num2);
        System.out.println(num1 * num2);
        System.out.println(num1 / num2);
        System.out.println(num1 % num2);
        
    }
    
}
