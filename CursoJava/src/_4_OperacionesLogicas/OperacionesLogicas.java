
package _4_OperacionesLogicas;

public class OperacionesLogicas {

    public static void main(String[] args) {
        
        // (expresion1) < (expresion2): menor
        // (expresion1) > (expresion2): mayor 
        // (expresion1) <= (expresion2): menor o igual
        // (expresion1) >= (expresion2): mayor o igual
        // (expresion1) == (expresion2): igualdad
        // (expresion1) === (expresion2): identico
        // (expresion1) =! (expresion2): distinto
        // (expresion1) && (expresion2): and
        // (expresion1) || (expresion2): or
        // !(expresion): not
        
        boolean valor = (6<6);
        System.out.println(valor);
        
    }
    
}
